# Arthematic Operation Module

# functions for basic arithmetic operations such as addition
def calculate_sum(num1, num2):
    return num1 + num2

# functions for basic arithmetic operations such as difference
def calculate_difference(num1, num2):
    return num1 - num2

# functions for basic arithmetic operations such as product

def calculate_product(num1, num2):
    return num1 * num2

# functions for basic arithmetic operations such as quotient

def calculate_quotient(num1, num2):
    return num1 / num2

# functions for basic arithmetic operations such as modulus 

def calculate_modulus(num1, num2):
    return num1 % num2