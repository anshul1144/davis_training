# Arthematic Operation Module

# functions to calculate sum
def calculate_sum(num1, num2):
    return (num1 + num2)

# functions to calculate difference
def calculate_difference(num1, num2):
    return (num1 - num2)

# functions to calculate product

def calculate_product(num1, num2):
    return (num1 * num2)

# functions to calculate quotient

def calculate_quotient(num1, num2):
    return (num1 / num2)

# functions to calculate modulus

def calculate_modulus(num1, num2):
    return (num1 % num2)